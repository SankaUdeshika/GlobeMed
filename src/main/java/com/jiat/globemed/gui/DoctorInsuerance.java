    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        if (billing == null || billing.getInsuranceClaim() == null) {
            JOptionPane.showMessageDialog(this, "No insurance claim selected", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        BillingManagementService billingManagement = new BillingManagementService();
        InsuranceClaim claim = billing.getInsuranceClaim();
        billingManagement.doctorApprove(billing);
        JOptionPane.showMessageDialog(this, "Admin approved. Claim status: " + claim.getClaimStatus());
        loadBillingTable();
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        if (evt.getClickCount() == 2 && jTable2.getSelectedRow() != -1) {
            String BillID = jTable2.getValueAt(jTable2.getSelectedRow(), 0).toString();
            billing = new BillingDAO().getBilling(Long.parseLong(BillID));

        }
    }//GEN-LAST:event_jTable2MouseClicked

