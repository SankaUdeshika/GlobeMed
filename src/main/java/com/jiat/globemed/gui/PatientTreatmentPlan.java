    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String treatmentText = jTextField1.getText();
        String description = jTextArea1.getText();
        String endDateStr = jTextField2.getText(); // e.g. "2025-08-31"

        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");

        Date startDate = new Date(); // current date
        Date endDate = null;
        try {
            endDate = sdf.parse(endDateStr);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Invalid date format! Please use yyyy-MM-dd");
            return; // stop execution if invalid
        }

        TreatmentPlan treatmentObject = new TreatmentPlan(
                patientObject,
                new StafDAO().findById(Dashboard.LoggedUserID),
                description,
                startDate,
                endDate
        );

        // ðŸ”¥ Save treatment to DB
        new TreatmentDAO().saveTreatment(treatmentObject);

        // Refresh UI
        refresh();
    }//GEN-LAST:event_jButton1ActionPerformed

